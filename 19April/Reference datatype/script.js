// Type Of Reference Datatype--->

//1.  Array
const numbers = [1,2,3,4,5,6,7]
console.log(numbers , typeof numbers);

// 2. OBJECT LITERAL 
const person = {
    name:"john doe",
    age: 34,
    city:"Bhopal",
};
console.log(person, typeof person);

// 3. FUNCTION
//function declaration
function sayHello(){
console.log("hello");
}
// function calling / invoke
sayHello();
console.log(sayHello, typeof sayHello);


